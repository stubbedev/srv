func TestRemoveExistingDir(t *testing.T) {
	cfg := newTestConfig(t)
	spec := Spec{PHPVersion: "8.3"}
	if err := WriteFiles(cfg, spec, "", "", ""); err != nil {
		t.Fatal(err)
	}
	fp := Fingerprint(spec.PHPVersion, spec.Extensions)
	dir := Dir(cfg, fp)
	if _, err := os.Stat(dir); err != nil {
		t.Fatalf("expected dir created: %v", err)
	}
	if err := Remove(cfg, fp); err != nil {
		t.Fatalf("Remove err: %v", err)
	}
	if _, err := os.Stat(dir); !os.IsNotExist(err) {
		t.Errorf("dir should be gone, stat err: %v", err)
	}
}

