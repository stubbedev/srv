func TestFingerprintDoesNotMutateInput(t *testing.T) {
	ext := []string{"redis", "imagick", "bcmath"}
	original := append([]string(nil), ext...)
	_ = Fingerprint("8.3", ext)
	for i := range original {
		if ext[i] != original[i] {
			t.Fatalf("Fingerprint mutated input: %v -> %v", original, ext)
		}
	}
}

