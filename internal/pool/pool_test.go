func TestWriteFilesCreatesAllArtifacts(t *testing.T) {
	cfg := newTestConfig(t)
	spec := Spec{
		PHPVersion: "8.3",
		Extensions: []string{"redis"},
		Members: []Member{
			{SiteName: "alpha", ProjectPath: "/srv/alpha"},
			{SiteName: "beta", ProjectPath: "/srv/beta"},
		},
	}
	if err := WriteFiles(cfg, spec, "FROM php", "memory_limit=512M\n", "[www]\n"); err != nil {
		t.Fatalf("WriteFiles err: %v", err)
	}
	fp := Fingerprint(spec.PHPVersion, spec.Extensions)
	dir := Dir(cfg, fp)

	for _, name := range []string{
		constants.PHPDockerfileFile,
		constants.PHPIniFile,
		constants.PHPFPMConfFile,
		"docker-compose.yml",
	} {
		if _, err := os.Stat(filepath.Join(dir, name)); err != nil {
			t.Errorf("expected %s to exist: %v", name, err)
		}
	}

	composeBytes, err := os.ReadFile(filepath.Join(dir, "docker-compose.yml"))
	if err != nil {
		t.Fatalf("read compose: %v", err)
	}
	if !strings.HasPrefix(string(composeBytes), "# Generated by srv") {
		t.Errorf("compose header missing; got: %q", string(composeBytes[:60]))
	}

	var parsed composeFile
	// Skip header lines (start at `services:`).
	yamlStart := strings.Index(string(composeBytes), "name:")
	if yamlStart < 0 {
		yamlStart = strings.Index(string(composeBytes), "services:")
	}
	if err := yaml.Unmarshal(composeBytes[yamlStart:], &parsed); err != nil {
		t.Fatalf("compose not valid YAML: %v", err)
	}
	if parsed.Name != constants.ComposeProjectName {
		t.Errorf("compose Name = %q, want %q", parsed.Name, constants.ComposeProjectName)
	}
	svc, ok := parsed.Services["fpm"]
	if !ok {
		t.Fatal("compose missing fpm service")
	}
	if svc.ContainerName != ContainerName(fp) {
		t.Errorf("ContainerName = %q, want %q", svc.ContainerName, ContainerName(fp))
	}
	if svc.Image != ImageTag(fp) {
		t.Errorf("Image = %q, want %q", svc.Image, ImageTag(fp))
	}
	// Two site volumes + 2 mount-bind volumes (php.ini, php-fpm.conf) = 4.
	if len(svc.Volumes) != 4 {
		t.Errorf("Volumes len = %d, want 4", len(svc.Volumes))
	}
}

func TestWriteFilesSortsMembers(t *testing.T) {
	cfg := newTestConfig(t)
	spec := Spec{
		PHPVersion: "8.3",
		Members: []Member{
			{SiteName: "zulu", ProjectPath: "/srv/zulu"},
			{SiteName: "alpha", ProjectPath: "/srv/alpha"},
			{SiteName: "mike", ProjectPath: "/srv/mike"},
		},
	}
	if err := WriteFiles(cfg, spec, "FROM", "", ""); err != nil {
		t.Fatalf("WriteFiles err: %v", err)
	}
	dir := Dir(cfg, Fingerprint(spec.PHPVersion, spec.Extensions))
	data, err := os.ReadFile(filepath.Join(dir, "docker-compose.yml"))
	if err != nil {
		t.Fatalf("read compose: %v", err)
	}
	body := string(data)
	idxAlpha := strings.Index(body, "/srv/alpha")
	idxMike := strings.Index(body, "/srv/mike")
	idxZulu := strings.Index(body, "/srv/zulu")
	if !(idxAlpha < idxMike && idxMike < idxZulu) {
		t.Errorf("members not sorted: alpha=%d mike=%d zulu=%d", idxAlpha, idxMike, idxZulu)
	}
}

func TestWriteFilesMkdirError(t *testing.T) {
	// Point Root at a path that contains a file, blocking MkdirAll.
	tmp := t.TempDir()
	conflict := filepath.Join(tmp, "blocker")
	if err := os.WriteFile(conflict, []byte{}, 0o644); err != nil {
		t.Fatal(err)
	}
	cfg := &config.Config{Root: conflict, NetworkName: "n"}
	err := WriteFiles(cfg, Spec{PHPVersion: "8.3"}, "", "", "")
	if err == nil {
		t.Fatal("expected mkdir error")
	}
	if !strings.Contains(err.Error(), "create pool dir") {
		t.Errorf("error %q missing wrapped context", err)
	}
}

