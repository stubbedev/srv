func TestFingerprintStable(t *testing.T) {
	a := Fingerprint("8.3", []string{"redis", "imagick"})
	b := Fingerprint("8.3", []string{"redis", "imagick"})
	if a != b {
		t.Errorf("Fingerprint not stable: %s != %s", a, b)
	}
	if len(a) != 12 {
		t.Errorf("Fingerprint length = %d, want 12", len(a))
	}
}

