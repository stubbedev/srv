func TestEnsureSiteComposeInclude(t *testing.T) {
	t.Run("adds include to file without include section", func(t *testing.T) {
		tmpDir := t.TempDir()
		content := `services:
  web:
    image: nginx
`
		composePath := filepath.Join(tmpDir, "docker-compose.yml")
		if err := os.WriteFile(composePath, []byte(content), 0o644); err != nil {
			t.Fatalf("failed to write compose file: %v", err)
		}

		added, err := EnsureSiteComposeInclude(composePath)
		if err != nil {
			t.Fatalf("EnsureSiteComposeInclude failed: %v", err)
		}
		if !added {
			t.Error("expected include to be added")
		}

		// Read and verify the file
		data, _ := os.ReadFile(composePath)
		if !containsString(string(data), "docker-compose.site.yml") {
			t.Error("expected file to contain docker-compose.site.yml")
		}
	})

	t.Run("does not duplicate existing include", func(t *testing.T) {
		tmpDir := t.TempDir()
		content := `include:
  - docker-compose.site.yml
services:
  web:
    image: nginx
`
		composePath := filepath.Join(tmpDir, "docker-compose.yml")
		if err := os.WriteFile(composePath, []byte(content), 0o644); err != nil {
			t.Fatalf("failed to write compose file: %v", err)
		}

		added, err := EnsureSiteComposeInclude(composePath)
		if err != nil {
			t.Fatalf("EnsureSiteComposeInclude failed: %v", err)
		}
		if added {
			t.Error("expected include to not be added (already present)")
		}
	})

	t.Run("adds to existing include section", func(t *testing.T) {
		tmpDir := t.TempDir()
		content := `include:
  - other-compose.yml
services:
  web:
    image: nginx
`
		composePath := filepath.Join(tmpDir, "docker-compose.yml")
		if err := os.WriteFile(composePath, []byte(content), 0o644); err != nil {
			t.Fatalf("failed to write compose file: %v", err)
		}

		added, err := EnsureSiteComposeInclude(composePath)
		if err != nil {
			t.Fatalf("EnsureSiteComposeInclude failed: %v", err)
		}
		if !added {
			t.Error("expected include to be added")
		}

		// Read and verify both includes are present
		data, _ := os.ReadFile(composePath)
		content = string(data)
		if !containsString(content, "docker-compose.site.yml") {
			t.Error("expected file to contain docker-compose.site.yml")
		}
		if !containsString(content, "other-compose.yml") {
			t.Error("expected file to still contain other-compose.yml")
		}
	})
}

func TestRemoveSiteComposeInclude(t *testing.T) {
	t.Run("removes include from file", func(t *testing.T) {
		tmpDir := t.TempDir()
		content := `include:
  - docker-compose.site.yml
services:
  web:
    image: nginx
`
		composePath := filepath.Join(tmpDir, "docker-compose.yml")
		if err := os.WriteFile(composePath, []byte(content), 0o644); err != nil {
			t.Fatalf("failed to write compose file: %v", err)
		}

		removed, err := RemoveSiteComposeInclude(composePath)
		if err != nil {
			t.Fatalf("RemoveSiteComposeInclude failed: %v", err)
		}
		if !removed {
			t.Error("expected include to be removed")
		}

		// Verify the include is gone and services remain
		data, _ := os.ReadFile(composePath)
		content = string(data)
		if containsString(content, "docker-compose.site.yml") {
			t.Error("expected docker-compose.site.yml to be removed")
		}
		if !containsString(content, "services:") {
			t.Error("expected services to still be present")
		}
	})

	t.Run("returns false for non-existent file", func(t *testing.T) {
		tmpDir := t.TempDir()
		composePath := filepath.Join(tmpDir, "non-existent.yml")

		removed, err := RemoveSiteComposeInclude(composePath)
		if err != nil {
			t.Fatalf("unexpected error: %v", err)
		}
		if removed {
			t.Error("expected removed to be false for non-existent file")
		}
	})

	t.Run("returns false when include not present", func(t *testing.T) {
		tmpDir := t.TempDir()
		content := `services:
  web:
    image: nginx
`
		composePath := filepath.Join(tmpDir, "docker-compose.yml")
		if err := os.WriteFile(composePath, []byte(content), 0o644); err != nil {
			t.Fatalf("failed to write compose file: %v", err)
		}

		removed, err := RemoveSiteComposeInclude(composePath)
		if err != nil {
			t.Fatalf("unexpected error: %v", err)
		}
		if removed {
			t.Error("expected removed to be false when include not present")
		}
	})

	t.Run("preserves other includes", func(t *testing.T) {
		tmpDir := t.TempDir()
		content := `include:
  - other-compose.yml
  - docker-compose.site.yml
services:
  web:
    image: nginx
`
		composePath := filepath.Join(tmpDir, "docker-compose.yml")
		if err := os.WriteFile(composePath, []byte(content), 0o644); err != nil {
			t.Fatalf("failed to write compose file: %v", err)
		}

		removed, err := RemoveSiteComposeInclude(composePath)
		if err != nil {
			t.Fatalf("RemoveSiteComposeInclude failed: %v", err)
		}
		if !removed {
			t.Error("expected include to be removed")
		}

		// Verify only docker-compose.site.yml is removed
		data, _ := os.ReadFile(composePath)
		content = string(data)
		if containsString(content, "docker-compose.site.yml") {
			t.Error("expected docker-compose.site.yml to be removed")
		}
		if !containsString(content, "other-compose.yml") {
			t.Error("expected other-compose.yml to remain")
		}
	})
}

