func TestParseEnv(t *testing.T) {
	tests := []struct {
		name           string
		content        string
		expectedDomain string
		expectedLocal  bool
	}{
		{
			name: "basic production domain",
			content: `DEPLOY_DOMAIN=example.com
DEPLOY_LOCAL=false`,
			expectedDomain: "example.com",
			expectedLocal:  false,
		},
		{
			name: "local domain with true",
			content: `DEPLOY_DOMAIN=myapp.test
DEPLOY_LOCAL=true`,
			expectedDomain: "myapp.test",
			expectedLocal:  true,
		},
		{
			name: "local domain with 1",
			content: `DEPLOY_DOMAIN=myapp.test
DEPLOY_LOCAL=1`,
			expectedDomain: "myapp.test",
			expectedLocal:  true,
		},
		{
			name: "with comments and blank lines",
			content: `# This is a comment
DEPLOY_DOMAIN=test.example.com

# Another comment
DEPLOY_LOCAL=true
`,
			expectedDomain: "test.example.com",
			expectedLocal:  true,
		},
		{
			name: "with extra whitespace",
			content: `  DEPLOY_DOMAIN  =  example.com  
  DEPLOY_LOCAL  =  true  `,
			expectedDomain: "example.com",
			expectedLocal:  true,
		},
		{
			name:           "missing DEPLOY_LOCAL defaults to false",
			content:        `DEPLOY_DOMAIN=example.com`,
			expectedDomain: "example.com",
			expectedLocal:  false,
		},
		{
			name:           "empty file",
			content:        ``,
			expectedDomain: "",
			expectedLocal:  false,
		},
		{
			name: "invalid lines ignored",
			content: `invalid line without equals
DEPLOY_DOMAIN=example.com
another invalid line
DEPLOY_LOCAL=true`,
			expectedDomain: "example.com",
			expectedLocal:  true,
		},
		{
			name: "with network config",
			content: `# Generated by srv
DEPLOY_DOMAIN=myapp.test
DEPLOY_LOCAL=true
DEPLOY_NETWORK=srv_traefik`,
			expectedDomain: "myapp.test",
			expectedLocal:  true,
		},
		{
			name: "domain with equals in value",
			content: `DEPLOY_DOMAIN=example.com=extra
DEPLOY_LOCAL=false`,
			expectedDomain: "example.com=extra",
			expectedLocal:  false,
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			// Create temporary directory and env.site file
			tmpDir := t.TempDir()
			envPath := filepath.Join(tmpDir, "env.site")
			if err := os.WriteFile(envPath, []byte(tt.content), 0o644); err != nil {
				t.Fatalf("failed to write env.site: %v", err)
			}

			domain, isLocal := ParseEnv(tmpDir)

			if domain != tt.expectedDomain {
				t.Errorf("domain = %q, want %q", domain, tt.expectedDomain)
			}
			if isLocal != tt.expectedLocal {
				t.Errorf("isLocal = %v, want %v", isLocal, tt.expectedLocal)
			}
		})
	}
}

